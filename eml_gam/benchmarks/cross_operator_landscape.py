"""Cross-operator landscape: does the depth-collapse appear in psi too?

The headline finding in ``benchmarks/landscape.py`` is that EML trees
fail to recover a depth-d target from random initialisation at d >= 3.
Odrzywolek's paper conjectures that this is intrinsic to the iterated
exp/log structure of the operator; the natural follow-up is to check
whether the *smooth* sibling ``psi(x, y) = sinh(x) - arsinh(y)`` shares
the same failure mode or, more interestingly, breaks it.

Target construction mirrors ``landscape.py``: at every depth d we use a
known-good snap which produces a non-degenerate function of the input.
We train both an EML and a Psi tree from random initialisation on the
same dataset and report the fraction of trials that drove the final MSE
below ``1e-3``. The question of exact-snap match is not meaningful
across operators (Psi has a different set of snap configurations); we
therefore report only the MSE-based success rate.

This experiment is the operator-comparative analogue of Odrzywolek's
Figure 4 / Table 2 and the landscape study of Section 2 of our README.
A clean answer in either direction is publishable: if Psi shows the
same collapse, the phenomenon is intrinsic to the iterated-Sheffer
structure and not to exp/log specifically; if Psi breaks the collapse,
it is a positive resolution to the open problem #1 of Odrzywolek (2026).
"""

from __future__ import annotations

import json
from dataclasses import asdict, dataclass

import numpy as np
import torch
import torch.nn as nn

from ..eml_tree import EMLTree
from ..sheffer import PsiTree
from ..utils import DTYPE
from .landscape import target_snap_elog_iterated_exp


@dataclass
class CrossResult:
    target_operator: str
    solver_operator: str
    depth: int
    n_trials: int
    n_low_mse: int
    success_rate: float


def _x_domain(depth: int, n: int = 256, seed: int = 0) -> torch.Tensor:
    rng = np.random.default_rng(seed)
    if depth == 1:
        x_np = rng.uniform(0.2, 3.0, size=n)
    elif depth == 2:
        x_np = rng.uniform(-1.5, 1.5, size=n)
    elif depth == 3:
        x_np = rng.uniform(-2.0, 0.5, size=n)
    elif depth == 4:
        x_np = rng.uniform(-3.0, -0.3, size=n)
    else:
        x_np = rng.uniform(-3.0, -0.8, size=n)
    return torch.tensor(x_np, dtype=DTYPE).unsqueeze(1)


def _target_data(
    depth: int, target_operator: str = "eml", n: int = 256, seed: int = 0,
) -> tuple[torch.Tensor, torch.Tensor]:
    """Build a depth-``d`` target with the specified operator.

    Both operators share the slot grammar; only the atom (``eml`` vs
    ``psi``) differs. The same snap configuration
    ``target_snap_elog_iterated_exp(depth)`` is evaluated through a tree
    of the chosen operator to yield an operator-native target.
    """
    x = _x_domain(depth, n=n, seed=seed)
    snap = target_snap_elog_iterated_exp(depth)
    if target_operator == "eml":
        tree: nn.Module = EMLTree(
            depth=depth, n_inputs=1, use_input_affine=False,
        )
    elif target_operator == "psi":
        tree = PsiTree(depth=depth, n_inputs=1)
    else:
        raise ValueError(f"unknown operator: {target_operator}")
    tree.set_snap_config(snap)
    with torch.no_grad():
        y = tree(x)
    # Some PsiTree snaps at deep x ranges can still hit numerical edges;
    # drop points that come back non-finite to keep the downstream
    # optimiser well-posed.
    mask = torch.isfinite(y)
    if not mask.all():
        x = x[mask]
        y = y[mask]
    return x, y


def _train_random(
    tree_cls, depth: int, x: torch.Tensor, y: torch.Tensor,
    n_epochs: int = 1000, seed: int = 0,
) -> float:
    torch.manual_seed(seed)
    kwargs = {"depth": depth, "n_inputs": 1}
    if tree_cls is EMLTree:
        kwargs["use_input_affine"] = False
    tree = tree_cls(**kwargs)
    optim = torch.optim.Adam(tree.parameters(), lr=5e-2)
    for _ in range(n_epochs):
        optim.zero_grad()
        pred = tree(x)
        mse = torch.mean((pred - y) ** 2)
        if not torch.isfinite(mse):
            return float("inf")
        mse.backward()
        nn.utils.clip_grad_norm_(tree.parameters(), 1.0)
        optim.step()
    with torch.no_grad():
        if hasattr(tree, "snap"):
            tree.snap()
        pred = tree(x)
        if not torch.isfinite(pred).all():
            return float("inf")
        return float(torch.mean((pred - y) ** 2).item())


def run_cross_operator(
    depths: tuple[int, ...] = (2, 3, 4, 5),
    n_trials: int = 20,
    n_epochs: int = 1000,
    mse_threshold_rel: float = 1e-3,
    verbose: bool = True,
) -> list[CrossResult]:
    """Run the full 2x2 operator-symmetric cross-landscape.

    For each depth we construct **two** targets — one generated by an
    ``eml``-tree, one generated by a ``psi``-tree, both from the same
    snap configuration — and then train each operator's tree from
    random initialisation on **each** target. The resulting 2x2 matrix
    isolates whether a solver's failure is due to the target being
    outside its native class ("off-diagonal" cells) or due to the
    landscape collapse itself ("on-diagonal" cells).

    ``mse_threshold_rel`` is the relative MSE threshold:
    ``final_mse < mse_threshold_rel * var(y_target)``. This makes
    comparison across operators fair even when the targets have
    different dynamic ranges.
    """
    out: list[CrossResult] = []
    if verbose:
        print(f"{'=' * 70}")
        print(
            "  Symmetric cross-operator landscape (operator-native targets)"
        )
        print(f"{'=' * 70}")
    for depth in depths:
        for target_op in ("eml", "psi"):
            x, y = _target_data(depth, target_operator=target_op)
            var_y = float(torch.var(y).item())
            threshold = mse_threshold_rel * max(var_y, 1e-12)
            for solver_op, tree_cls in (("eml", EMLTree), ("psi", PsiTree)):
                n_low = 0
                for trial in range(n_trials):
                    final_mse = _train_random(
                        tree_cls, depth, x, y,
                        n_epochs=n_epochs, seed=trial * 31 + 7,
                    )
                    if np.isfinite(final_mse) and final_mse < threshold:
                        n_low += 1
                result = CrossResult(
                    target_operator=target_op,
                    solver_operator=solver_op,
                    depth=depth,
                    n_trials=n_trials, n_low_mse=n_low,
                    success_rate=n_low / n_trials,
                )
                out.append(result)
                if verbose:
                    tag = (
                        "on-diag" if target_op == solver_op else "off-diag"
                    )
                    print(
                        f"  d={depth}  target={target_op:3s} "
                        f"solver={solver_op:3s}  [{tag}]  "
                        f"{n_low:2d}/{n_trials} ({result.success_rate:.0%})"
                    )
    return out


def save(results: list[CrossResult], path: str) -> None:
    with open(path, "w") as f:
        json.dump([asdict(r) for r in results], f, indent=2)


if __name__ == "__main__":
    results = run_cross_operator()
    save(results, "cross_operator_landscape.json")
